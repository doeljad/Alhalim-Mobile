    <p><br />
        ““Dua belas rakaat yang ditunaikan seseorang maka sebuah rumah di surga akan dibangunkan untuknya, yakni empat rakaat sebelum dhuhur, dua rakaat setelah dhuhur, dua rakaat sebelum ashar, dua rakaat setelah magrib, dan dua rakaat sebelum Subuh,” tulis Ustadz Tatam mengutip riwayat an-Nasai dari Ummu Habibah.</p>
