    <p>Jakarta, <strong><em>NU Online</em></strong></p>
    <p>Umat Islam wajib melaksanakan shalat wajib lima kali sehari, yakni Dzuhur, Ashar, Maghrib, Isya, dan Subuh. Hal ini sebagaimana perintah Allah swt yang termaktub dalam sejumlah ayat di Al-Qur’an.</p>
    <p><br />
        Dalam menjalankan shalat lima waktu itu, umat Islam juga dianjurkan untuk melaksanakan shalat sunnah sebelum dan sesudahnya yang disebut qabliyah dan ba'diyah. </p>
