    <p><br />
        Dalam shalat Dzuhur, umat Islam dianjurkan untuk melaksanakan shalat sunnah qabliyah dua atau empat rakaat. Pun shalat sunnah bakdiyah juga dianjurkan dilakukan dua atau empat rakaat. Dua rakaat sebelum dan sesudah shalat Dzuhur itu hukumnya sunnah muakkadah (sangat dianjurkan), sedangkan dua tambahannya menjadi empat rakaat adalah sunnah yang ghairu muakkadah (tidak begitu dianjurkan).</p>
    <p><br />
        Hal tersebut sebagaimana dijelaskan Ustadz Tatam Wijaya dalam artikelnya berjudul <em><a href="https://nu.or.id/shalat/keutamaan-shalat-sunnah-rawatib-nhU1V" target="_blank">Keutamaan Shalat Sunnah Rawatib</a></em> yang dikutip <em>NU Online</em> pada Sabtu (24/8/2024).</p>
