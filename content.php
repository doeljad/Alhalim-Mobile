    <p><br />
        Tidak hanya itu, umat Islam yang dapat melaksanakan shalat sunnah qabliyah dan ba'diyah Dzuhur, secara khusus, juga bakal dibebaskan dari siksa neraka.</p>
    <p><br />
        “Siapa orang yang menjaga empat rakaat sebelum Zuhur dan empat rakaat setelahnya, maka Allah haramkan ia masuk neraka,” tulis Ustadz Ahmad Dirgahayu Hidayat mengutip sebuah hadits dalam tulisannya berjudul <a href="https://nu.or.id/syariah/tata-cara-shalat-rawatib-niat-dalil-waktu-dan-fungsinya-ga01H" target="_blank"><em>Tata Cara Shalat Rawatib: Niat, Dalil, Waktu, dan Fungsinya</em></a>.</p>
